package booleans;

public class BooleanExampel {

	/*
	 * 
	 * Java Booleans
	 * 
	 * Very often, in programming, you will need a data type that can only have one
	 * of two values, like:
	 * 
	 * YES / NO ON / OFF TRUE / FALSE
	 * 
	 * For this, Java has a boolean data type, which can take the values true or
	 * false.
	 * 
	 * 
	 * 
	 */

	public static void main(String[] args) {

		int x = 10;
		int y = 9;
		System.out.println(x > y); // returns true, because 10 is higher than 9

		System.out.println(8 > 9); // returns true, because 10 is higher than 9

	}

}
