package datatypes;

public class Datatypes {

	/*
	 * Non-primitive data types are called reference types because they refer to
	 * objects.
	 * 
	 * The main difference between primitive and non-primitive data types are:
	 * 
	 * Primitive types are predefined (already defined) in Java. Non-primitive types
	 * are created by the programmer and is not defined by Java (except for String).
	 * Non-primitive types can be used to call methods to perform certain
	 * operations, while primitive types cannot. A primitive type has always a
	 * value, while non-primitve types can be null. A primitive type starts with a
	 * lowercase letter, while non-primitive types starts with an uppercase letter.
	 * The size of a primitive type depends on the data type, while non-primitive
	 * types have all the same size.
	 * 
	 * Examples of non-primitive types are String, Arrays, Classes, Interface, etc.
	 * You will learn more about these in a later chapter.
	 * 
	 */

	public static void main(String[] args) {

		// Primitive data types

		byte num; // Stores whole numbers from -128 to 127
		short num1; // Stores whole numbers from -32,768 to 32,767
		int num2; // Stores whole numbers from -2,147,483.648 to 2,147,483,647
		long num3; // Stores whole numbers from -9,223.372,036.854,775.808 to
					// 9,223.372,036,854,775,808
		float num4; // Stores fractional numbers from 3.4e−038 to 3.4e+038. Sufficient for storing 6
					// to 7 decimal digits
		double num5; // Stores fractional numbers from 3.4e−038 to 3.4e+038. Sufficient for storing 6
						// to 7 decimal digits
		boolean myBool; // Stores true or false values
		char myLetter; // Stores a single character/letter

	}

}
